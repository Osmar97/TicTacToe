public abstract class Jogo {

   private int tabuleiro;
   private int vezDe;

    public int getTabuleiro() {
        return tabuleiro;
    }

    public void setTabuleiro(int tabuleiro) {
        this.tabuleiro = tabuleiro;
    }

    public int getVezDe() {
        return vezDe;
    }

    public void setVezDe(int vezDe) {
        this.vezDe = vezDe;
    }
}
